const mongoose = require('mongoose');
const Schema = mongoose.Schema;

const requestAdminSchema = new Schema({
  username: {
    type: String,
    required: true,
  },
  status: {
    type: String,
    enum: ['pending', 'approved', 'rejected'],
    default: 'pending',
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
});

module.exports = mongoose.model('RequestAdmin', requestAdminSchema);
